#include <stdio.h>

/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: The addition of both integers
 */

int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - difference of two integers
 * @a: first integer
 * @b: second integer
 * Return: The difference of both integers
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 * Return: The product of both integers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 * Return: The division of both integers
 */

int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - The modulus two integers
 * @a: first integer
 * @b: second integer
 * Return: The remainder of both integers
 */

int mod(int a, int b)
{
	return (a % b);
}
